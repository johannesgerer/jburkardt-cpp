  {
    t = pi * ( float ) ( i ) / ( float ) ( n_circle - 1 );
    x[i] = cos ( t );
    y[i] = sin ( t );
  }
  thkcrv ( 10 );
  curve ( x, y, n_circle );
//
//  End this plot.
//
  endgrf ( );
//
//  Free memory.
//
  delete [] x;
  delete [] xy_in;
  delete [] xy_out;
  delete [] y;
//
