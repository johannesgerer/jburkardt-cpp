int main ( )

//****************************************************************************80
//
//  Purpose:
//
//    MAIN is the main program for TSP_BRUTE.
//
//  Licensing:
//
//    This code is distributed under the GNU LGPL license.
//
//  Modified:
//
//    04 December 2013
//
//  Author:
//
//    John Burkardt
//
{
  double *distance;
  string distance_filename;
  int i;
  int i1;
  int i2;
  int m;
  bool more;
  int n;
  int *p;
  int *p_min;
  int paths;
  double total;
  double total_ave;
  double total_max;
  double total_min;

  timestamp ( );
  cout << "\n";
  cout << "TSP_BRUTE:\n";
  cout << "  C++ version\n";
  cout << "  Solve small traveling salesman problems by brute force.\n";
//
//  Get the distance table.
//
  cout << "\n";
  cout << "  Enter the name of the file containing\n";
  cout << "  the city-to-city distance table.\n";

  cin >> distance_filename;

  r8mat_header_read ( distance_filename, m, n );

  cout << "\n";
  cout << "  Number of cities is " << n << "\n";

  distance = r8mat_data_read ( distance_filename, n, n );
//
//  Print the distance matrix.
//
  r8mat_print ( n, n, distance, "  Distance matrix:" );
//
//  Examine every permutation.
//
  total_max = - r8_huge ( );
  total_min = r8_huge ( );
  total_ave = 0.0;

  more = false;
  p = new int[n];
  p_min = new int[n];

  paths = 0;

  for ( ; ; )
  {
    perm_next3 ( n, p, more );

    paths = paths + 1;

    total = 0.0;
    i1 = n - 1;
    for ( i2 = 0; i2 < n; i2++ )
    {
      total = total + distance[p[i1]+p[i2]*n];
      i1 = i2;
    }

    total_ave = total_ave + total;

    if ( total_max < total )
    {
      total_max = total;
    }

    if ( total < total_min )
    {
      total_min = total;
      for ( i = 0; i < n; i++ )
      {
        p_min[i] = p[i];
      }
    }

    if ( ! more )
    {
      break;
    }

  }

  total_ave = total_ave / ( double ) ( paths );
  
  cout << "\n";
  cout << "  Itinerary:\n";
  cout << "  Step  From  To    Distance\n";
  cout << "\n";
  i1 = n - 1;
  for ( i2 = 0; i2 < n; i2++ )
  {
    cout << "  " << setw(4) << i2
         << "    " << setw(2) << p_min[i1]
         << "  " << setw(2) << p_min[i2]
         << "  " << distance[p_min[i1]+p_min[i2]*n] << "\n";
    i1 = i2;
  }
  cout << "\n";
  cout << "  Total:         " << total_min << "\n";

  cout << "\n";
  cout << "  Number of paths checked = " << paths << "\n";
  cout << "\n";
  cout << "  Minimum length = " << total_min << "\n";
  cout << "  Average length = " << total_ave << "\n";
  cout << "  Maximum length = " << total_max << "\n";
//
//  Free memory.
//
  delete [] distance;
  delete [] p;
  delete [] p_min;
/*
  Terminate.
*/
  cout << "\n";
  cout << "TSP_BRUTE\n";
  cout << "  Normal end of execution.\n";
  cout << "\n";
  timestamp ( );

  return 0;
}
