//****************************************************************************80
//
//  Purpose:
//
//    XML_MESH1D_WRITE writes a 1D mesh as a DOLFIN XML file.
//
//  Licensing:
//
//    This code is distributed under the GNU LGPL license.
//
//  Modified:
//
//    05 October 2014
//
//  Author:
