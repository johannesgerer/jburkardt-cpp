  cout << "\n";

  for ( i = 1; i <= 10; i++ )
  {
    r = r4_random ( &s1, &s2, &s3 );
    cout << "  " << setw(8) << i
         << "  " << setw(14) << r
         << "  " << setw(12) << s1
         << "  " << setw(12) << s2
         << "  " << setw(12) << s3 << "\n";

    if ( i == 5 )
    {
      s1_save = s1;
      s2_save = s2;
      s3_save = s3;
    }
  }
  s1 = s1_save;
  s2 = s2_save;
  s3 = s3_save;

  cout << "\n";
  cout << "  Restart the sequence, using the seeds\n";
  cout << "  produced after step 5:\n";
  cout << "\n";
  cout << "  S1 = " << s1 << "\n";
  cout << "  S2 = " << s2 << "\n";
  cout << "  S3 = " << s3 << "\n";
  cout << "\n";
  cout << "         I      R                     S1            S2            S3\n";
  cout << "\n";

  for ( i = 1; i <= 10; i++ )
  {
    r = r4_random ( &s1, &s2, &s3 );
    cout << "  " << setw(8) << i
         << "  " << setw(14) << r
         << "  " << setw(12) << s1
         << "  " << setw(12) << s2
         << "  " << setw(12) << s3 << "\n";
  }
  return;
