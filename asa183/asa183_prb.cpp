  cout << "TEST03\n";
  cout << "  Show how the seeds used by R4_RANDOM,\n";
  cout << "  which change on each step, can be reset to\n";
  cout << "  restore any part of the sequence.\n";

  s1_save = 12345;
  s2_save = 34567;
  s3_save = 56789;

  s1 = s1_save;
  s2 = s2_save;
  s3 = s3_save;

